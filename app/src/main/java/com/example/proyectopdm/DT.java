package com.example.proyectopdm;

public class DT {
    int id, idU;

    public DT() {
    }

    public DT(int id, int idU) {
        this.id = id;
        this.idU = idU;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdU() {
        return idU;
    }

    public void setIdU(int idU) {
        this.idU = idU;
    }
}
